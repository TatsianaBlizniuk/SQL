/* Таблица поставщиков */
CREATE TABLE PROVIDERS
(
PROVIDER VARCHAR(20) NOT NULL,
ADRESS VARCHAR(100) NOT NULL,
PHONE VARCHAR(15) NOT NULL,
);
ALTER TABLE PROVIDERS
ADD CONSTRAINT PROVIDERS_PK PRIMARY KEY (PROVIDER);
--DROP TABLE PROVIDERS;



/* Таблица продукции */
CREATE TABLE PRODUCTS
(
NUMBER INT NOT NULL,
PRODUCT VARCHAR(200) NOT NULL,
QUANTITY_SHOP INT,
);
ALTER TABLE PRODUCTS
ADD CONSTRAINT PRODUCTS_PK PRIMARY KEY (NUMBER);
--DROP TABLE PRODUCTS;



/* Таблица заказов */
CREATE TABLE ORDERSS
(
NUMBER INT NOT NULL,
PROVIDER VARCHAR(20) NOT NULL,
PRODUCT VARCHAR(200) NOT NULL,
DATE VARCHAR(15) NOT NULL,
PRICE MONEY,
QUANTITY INT,
CUSTOMER VARCHAR(100) NOT NULL,
);
--DROP TABLE ORDERSS;
ALTER TABLE ORDERSS
  ADD CONSTRAINT ORDERSS_PROVIDER_FK FOREIGN KEY (PROVIDER)
  REFERENCES PROVIDERS (PROVIDER);
ALTER TABLE ORDERSS
  ADD CONSTRAINT ORDERSS_NUMBER_FK FOREIGN KEY (NUMBER)
  REFERENCES PRODUCTS (NUMBER);


/* Таблица цен*/
CREATE TABLE PRICE
(
PROVIDER VARCHAR(20) NOT NULL,
PRODUCT VARCHAR(200) NOT NULL,
NUMBER INT NOT NULL,
PRICE MONEY,
);
--DROP TABLE PRICE;
ALTER TABLE PRICE
  ADD CONSTRAINT PRICE_NUMBER_FK FOREIGN KEY (NUMBER)
  REFERENCES PRODUCTS (NUMBER);
ALTER TABLE PRICE
  ADD CONSTRAINT PRICE_PROVIDER_FK FOREIGN KEY (PROVIDER)
  REFERENCES PROVIDERS (PROVIDER);



